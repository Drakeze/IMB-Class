# Python Variables and Data Types

# Numbers
age = 25               # Integer
height = 1.75          # Float
complex_number = 3 + 2 # Complex

# Strings
name = "John"          # String with double quotes
city = 'New York'      # String with single quotes
description = """This is a
multi-line string"""   # Multi-line string

# Boolean
is_student = True      # Boolean True
has_car = False        # Boolean False

# Lists (mutable)
fruits = ['apple', 'banana', 'orange']
numbers = [1, 2, 3, 4, 5]

# Tuples (immutable)
coordinates = (10, 20)
rgb_color = (255, 128, 0)

# Dictionaries
person = {
    'name': 'John',
    'age': 25,
    'city': 'New York'
}

# Sets
unique_numbers = {1, 2, 3, 4, 5}

# None type
empty_value = None